import UIKit

extension UIColor {
    
    class var green: UIColor {
        return UIColor(named: "GreenColor")!
    }
    
    class var yellow: UIColor {
        return UIColor(named: "YellowColor")!
    }
    
    class var red: UIColor {
        return UIColor(named: "RedColor")!
    }
}
